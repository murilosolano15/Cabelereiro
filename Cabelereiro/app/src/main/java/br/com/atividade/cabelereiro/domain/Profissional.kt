package br.com.atividade.cabelereiro.domain

class Profissional {

    var idprofissional:Int
    get() = idprofissional
    set(value) {idprofissional=value}


    var nome:String
    get() = nome
    set(value) {nome=value}


    var funcao:String
    get() = funcao
    set(value) {funcao=value}


    var idendereco:Int
    get() = idendereco
    set(value) {idendereco=value}

    var idcontato:Int
    get() = idcontato
    set(value) {idcontato=value}

    var idusuario:Int
    get() = idusuario
    set(value) {idusuario=value}
}